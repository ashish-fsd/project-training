export class Maths {
    constructor() {}

    sum(firstNumber, secondNumber) {
        return firstNumber + secondNumber;
    };

    // Multiply
    multiply(firstnumber,secondnumber)
    {

        return firstnumber*secondnumber;
    }
    // Divide

    divide(firstnumber,secondnumber)
    {
        return firstnumber/secondnumber;
    }
    // square Root
     squareroot(number)
     {
         return Math.sqrt(number);
     }
    
}
